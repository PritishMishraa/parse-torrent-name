"use strict";

var normalizeTitle = function (value) {
  return String(value || "")
    .normalize("NFKD")
    .replace(/[\u0300-\u036f]/g, "")
    .replace(/&/g, " and ")
    .replace(/[^a-zA-Z0-9]+/g, " ")
    .trim()
    .replace(/\s+/g, " ")
    .toLowerCase();
};

module.exports = normalizeTitle;
module.exports.normalizeTitle = normalizeTitle;
